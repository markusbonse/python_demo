
def special_function_final(x: float | int, y: float | int,) -> float | int:
    """Return x squared plus y.

    Parameters
    ----------
    x : numeric
        Base value to be squared.
    y : numeric
        Value to be added to x**2.

    Returns
    -------
    numeric
        The result of x**2 + y.
    """
    return x**2 + y
